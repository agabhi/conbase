package com.derive.conbase.model;

public class RecordAttribute {
	private Long recordId;
	private Long recordTypeAttributeId;
	private String value;
	
	public Long getRecordId() {
		return recordId;
	}
	public void setRecordId(Long recordId) {
		this.recordId = recordId;
	}
	public Long getRecordTypeAttributeId() {
		return recordTypeAttributeId;
	}
	public void setRecordTypeAttributeId(Long recordTypeAttributeId) {
		this.recordTypeAttributeId = recordTypeAttributeId;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
