package com.derive.conbase.model;

public class LayerChartEntry {
	private int layerAttributeConfigId;
	private int from;
	private int to;
	private int level;
	public int getFrom() {
		return from;
	}
	public void setFrom(int from) {
		this.from = from;
	}
	public int getTo() {
		return to;
	}
	public void setTo(int to) {
		this.to = to;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getLayerAttributeConfigId() {
		return layerAttributeConfigId;
	}
	public void setLayerAttributeConfigId(int layerAttributeConfigId) {
		this.layerAttributeConfigId = layerAttributeConfigId;
	}
	
	
	
}
